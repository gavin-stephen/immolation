package org.untitled.immolation.client.gui;

public class PersistentLines {
    public float x1, y1, x2, y2, width;
    public int color;

    public PersistentLines(float x1, float y1, float x2, float y2, float width, int color) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
        this.width = width;
        this.color = color;
    }
}
