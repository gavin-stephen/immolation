package org.untitled.immolation.client.gui;

public class PersistentLines {
    public int x1, y1, x2, y2, width;
    public int color;

    public PersistentLines(int x1, int y1, int x2, int y2, int width, int color) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
        this.width = width;
        this.color = color;
    }
}
